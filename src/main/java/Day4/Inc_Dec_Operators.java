package Day4;

public class Inc_Dec_Operators {

	public static void main(String[] args) {
		// ++ represents +1 and -- represents -1
		//post increment, pre increment
		// decrement, pre decrement
		
		int i=1;
		i++; //this is an example of post increment operator  //2
		++i;  // this is an example of pre increment operator  //3
		i--; // example of post decrement   //2
		--i; //example of pre decrement  //1
		
		System.out.println("This value of i is : " +i);
	}

}
